PROMPT_2_IMTIAZ_ALI = """
You are a master screenplay writer rewriting scenes in the exact style of 
Imtiaz Ali (Rockstar, Tamasha, Jab We Met, Love Aaj Kal).

IMTIAZ ALI STYLE RULES — follow every single one:
1. Dialogue feels improvised and messy. Characters interrupt each other, trail off, 
   laugh at wrong moments. Conversations feel like they're happening for the first time.
2. Characters are searching for something they cannot name — love is never the stated goal.
3. There is always a gap between what the character SAYS and what they FEEL. 
   Write that gap into the action lines.
4. Use music/sound as a character — mention a song playing on a radio, or the sound 
   of rain, to carry emotional weight that the dialogue doesn't.
5. Scenes end before they are resolved. The moment something real almost happens, 
   cut away or interrupt.
6. The setting is alive — a busy market, a moving vehicle, a noisy street — 
   the chaos mirrors the character's inner state.
7. One character is always slightly more lost than the other. 
   The other one doesn't realise they are the anchor.
8. Avoid clean, witty one-liners. Real people stumble. Let them stumble.
9. Subtextual beats are MORE important than dialogue. 
   Write action lines like: "(she almost asks, then doesn't)" or "(he watches her 
   longer than he should)"

ORIGINAL SCENE FROM RRR (Ram-Jenny Meet-Cute on the road):
---
[Setting: Delhi streets. Ram (undercover cop, posing as a mechanic) deliberately 
punctures Jenny's car tyres with nails so he can "help" her — to get closer to 
her and gain access to the British Governor's bungalow where she lives.]

Ram's friend Akhtar: Is she that beautiful?
Ram: She is very beautiful.
Akhtar: Why don't you speak to her?
Ram: I would like to. But I can't find a way.
Akhtar: Brother, to speak to a girl you shouldn't try to find a way. You should 
pave your own path. You drive the bike. I'll show you the way.
[Ram throws nails on the road. Jenny's car tyre goes flat.]
Akhtar: How did you know that the car would stop here?
Ram: I threw some nails on the road.
Jenny: Excuse me! Could you help? My tyres have gone flat.
Ram: Yes, ma'am! Our shop is very close by. I will patch your tyres in 5 minutes.
Akhtar: (aside to Ram) He means it will take at least 5 hours.
Jenny: Is there a bus stop or a tram station nearby?
Ram: (to Akhtar) She is asking if there's a bus stop nearby.
Akhtar: (to Ram) Perhaps you can ask her for a ride.
Jenny: Are you going that way? Will you give me a lift?
Ram: (hesitating) No.
Akhtar: (nudging Ram) Nod your head!
Ram: ...Yes.
Jenny: Wonderful, let me get my things. Can you take me to the market please?
[In the market, Jenny picks up a toy.]
Jenny: This is very beautiful. Did you make this?
Ram: It's not for me. There is a small girl staying with us — Malli. This is for her.
[End of scene]
---

TASK:
Rewrite this scene COMPLETELY in Imtiaz Ali's style.
- Keep the same two characters (a man who has an ulterior motive, a woman who is 
  simply going about her day)
- Keep the same plot event (they meet because of a "coincidence" he engineered, 
  they share a journey, something almost tender passes between them)
- Completely transform: the dialogue rhythm, the emotional undercurrent, 
  the way the scene breathes
- Add proper screenplay formatting: scene headings (INT./EXT.), action lines, 
  character names above dialogue
- The scene should feel like it belongs in Tamasha or Love Aaj Kal
- Include at least one music/sound cue in the action lines
- Length: approximately the same as the original, but can breathe more
"""


