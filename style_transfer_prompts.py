# PROMPT 3 — THE UNDERCOVER MISSION (Chunk 6) → Rewritten as MANI RATNAM
# ══════════════════════════════════════════════════════════════════════════════

PROMPT_3_MANI_RATNAM = """
You are a master screenplay writer rewriting scenes in the exact style of 
Mani Ratnam (Roja, Dil Se, Bombay, Raavanan, Ponniyin Selvan).

MANI RATNAM STYLE RULES — follow every single one:
1. Dialogue is restrained, poetic, and elliptical. Characters NEVER say what they mean 
   directly. Emotion is communicated through what is NOT said.
2. Every scene has a central visual metaphor — find one and weave it through 
   the action lines (water, fire, light, shadow, a specific object).
3. Political reality and personal emotion are ALWAYS intertwined. 
   A conversation about a mission must also be about something personal.
4. Silences are as important as dialogue. Mark them: "(a long silence)", "(she 
   looks away)", "(he doesn't answer)".
5. Scene descriptions are lyrical — the environment reflects the characters' 
   inner state. Rain = grief. Sunlight through trees = fragile hope.
6. Characters rarely look at each other when they say the most important things.
7. Lines should be SHORT. Remove every word that is not necessary. 
   If a line can be cut to half, cut it.
8. The antagonist (or authority figure) is never pure evil — they believe in 
   something, however wrong. Write that conviction into them.
9. End the scene on an image, not a line of dialogue.

ORIGINAL SCENE FROM RRR (Bheem hiding his identity + Ram's briefing):
---
[Setting: Bheem is hiding with a Muslim family in Delhi. He is injured after being 
beaten by British soldiers.]

Family member: He is a monster. Look how badly he has beaten him.
Bheem: (to the family) Why are you hiding your anger, son? If they come to know 
the truth about me... they will punish you all for giving me shelter. Even though 
I wasn't born to you, you are protecting me with your life. I shouldn't cause you 
any harm. Come what may, I'll never let anyone know my true identity.

[Cut to: British police briefing room]
British Officer: We have a hunter targeting the Governor roaming free in Delhi. 
We shouldn't really be bothering about imbecile tribals. However our good friend 
the Nizam seems to think so. And since this is a matter regarding the Governor 
we should act on it.
Sub-officer: Alright sir. We will apprehend this bugger. I would rather roast 
this swine on a bed of coals.
Officer: Well yes that is the catch. We have nothing on him. No identifying 
features. No criminal history. It's quite an impossible task.
Sub-officer: How the hell are we supposed to catch him?
Ram: (stepping forward) We don't need to search all over Delhi. What he wants 
is in the Governor's bungalow. He will come there. He will think that his 
enemy's enemies are his friends. We should also act like we are against the 
British.

[End of scene]
---

TASK:
Rewrite this scene COMPLETELY in Mani Ratnam's style.
- Keep the same two-part structure: (1) a fugitive hiding, saying goodbye, 
  (2) the authorities planning how to catch him
- Keep the same emotional core: a man protecting others by erasing himself; 
  a system trying to find and destroy something it cannot understand
- Completely transform: the dialogue (make it sparse and poetic), the scene 
  descriptions (make them lyrical), the emotional register (restrained, not epic)
- Add proper screenplay formatting: scene headings (INT./EXT.), action lines, 
  character names above dialogue
- Choose ONE visual metaphor and run it through both halves of the scene
- The scene should feel like it belongs in Roja or Dil Se
- End on a visual image, not dialogue
- Length: can be shorter than the original — Mani Ratnam believes in economy
"""
