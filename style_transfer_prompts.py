# PROMPT 1 — THE SHEPHERD SPEECH (Chunk 4) → Rewritten as ANURAG KASHYAP
# ══════════════════════════════════════════════════════════════════════════════

PROMPT_1_ANURAG_KASHYAP = """
You are a master screenplay writer rewriting scenes in the exact style of 
Anurag Kashyap (Gangs of Wasseypur, Black Friday, Ugly).

ANURAG KASHYAP STYLE RULES — follow every single one:
1. Dialogue is street-level, raw, and dark. Characters use UP/Bihar slang flavour 
   even in English. Short, punchy sentences. No oratory.
2. Violence or threat is communicated through MUNDANE conversation, not speeches. 
   Characters talk about chai or weather while implying murder.
3. Dark humour is always present — even in the most threatening scenes.
4. Characters never make grand declarations. They understate everything.
5. Scene descriptions are gritty: flickering bulbs, stained walls, cheap cigarettes, 
   crackling radio in background.
6. No clean resolution. Scenes end mid-thought or with an uncomfortable pause.
7. The powerful character is NEVER overtly threatening — they are casual, almost bored. 
   That is what makes them scary.
8. Replace any epic/mythic language with blunt, colloquial phrasing.

ORIGINAL SCENE FROM RRR (The Shepherd Speech):
---
[Setting: British administrative office, Delhi. A Nizam advisor named Venkat Avadhani 
comes to warn the British about a tribal shepherd (Bheem) who has come to Delhi to 
retrieve a kidnapped girl.]

Venkat: Greetings, sir. I am Mr Venkat Avadhani. Special advisor to the Nizam.

Edward: Governor Scott is not in India. State the purpose of your visit.

Venkat: When Governor Scott visited Adilabad recently he brought back a little girl. 
Our Nawab has sent me to tell you something about it.

Edward: It's regarding the girl we brought from the Deccan? It is our police 
department's opinion that the girl be returned to her village.

Venkat: She is a Gond child, sir. They are an innocent people. Even if you oppress 
them they won't raise their voice. But they have a trait — they like staying in herds 
like sheep. Even if one lamb goes missing it causes them great distress. And so the 
herd has a shepherd. He protects the herd with his life.

Edward: So this tribal is going to shoot down the mighty British empire with his bow 
and arrows?

Venkat: Don't misunderstand me. I'm only telling you about him. The shepherd will 
travel however far to retrieve the missing lamb. Be it morning or night, sun or rain, 
rocks, mountains, valleys, peaks — he will search anywhere and everywhere. And when 
he finds it — if the lamb is in the tiger's mouth — he will break its teeth, pry its 
jaws open, and take the lamb back. It seems the shepherd has come to Delhi to begin 
his hunt.

[End of scene]
---

TASK:
Rewrite this scene COMPLETELY in Anurag Kashyap's style.
- Keep the same characters (a local advisor warning a British officer about a dangerous 
  man coming to Delhi)
- Keep the same plot event (the warning)
- Completely transform: the dialogue tone, the scene setting, the way the threat is 
  communicated
- Add proper screenplay formatting: scene headings (INT./EXT.), action lines, 
  character names above dialogue
- The scene should feel like it belongs in Gangs of Wasseypur or Black Friday
- Length: approximately the same as the original
"""


